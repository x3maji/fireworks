<p>В зависимости от высоты подъема фейерверочных эффектов и места проведения шоу, все фейерверки разделяются на несколько основных видов.</p>
<br>
 <h2>Сценическая пиротехника</h2><br>
<p>Это очень популярный вид пиротехнического шоу в силу своей простоты и дешевизны. Самый распространенный вариант сценического фейерверка - фонтаны холодного огня (сценические, или "концертные" фонтаны). Данный вид фейерверка используют внутри помещений: на сцене, танцплощадке или в банкетном зале.</p>.

<h3>Сценические фонтаны</h3><br>
<p>Это наиболее популярный вид сценической пиротехники. Альтернативные названия - "фонтаны холодного огня", "фонтаны зальные", "малодымные фонтаны". Фонтан создает мощный поток, или "форс" одноцветных, как правило золотых или серебряных искр, высотой от 1.5 до 6 метров, длительностью от 20 до 60 секунд. Отличительная черта такого фонтана - низкая температура пламени и искр, а также отсутствие дыма или неприятного запаха. Если поднести руку к фонтану "холодных искр", вы почуствуете лишь умеренное тепло. Никакого риска обжечься искрами нет. Более того, все искры полностью прогорают в воздухе, на полу не остается каких-либо следов или мусора. Такие фонтаны, укрепленные на деревянной подставке, пиротехник может устанавливать прямо на паркет или ковер, не опасаясь повредить дорогостоящее покрытие. Зона безопасности холодных фонтанов невелика (она зависит в первую очередь от высоты искристого форса), поэтому их можно использовать в ограниченных помещениях ресторана, рядом с ковровой дорожкой, или на сцене, недалеко от декоративных элементов из цветов и воздушных шариков.</p>
<br>
<h3>Возможные сценарии использования холодных фонтанов:</h3><br>

<p>•	Выход молодых. Фонтаны устанавливаются в два ряда по бокам от ковровой дорожки и запускаются при появлении молодоженов;</p>
<p>•	Первый танец. Запуск холодных фонтанов осуществляется в наиболее эмоциональный момент музыкальной композиции, или во время припева;</p>
<p>•	Молодые уезжают из ресторана. Фонтаны устанавливаются позади лимузина; как только машина трогается с места, фонтаны активируются, имитируя "огненную дорожку" от колес, как в голливудских фильмах;</p>
<p>•	На сцене во время проведения концерта, презентации или шоу. Холодные фонтаны используются во время кульминации шоу-программы, при появлении важных гостей, при демонстрации нового продукта;</p>
<p>•	Вынос праздничного торта. Отличным сочетанием будут небольшие холодные фонтаны, горящие на самом торте, и одновременный запуск холодных фонтанов в помещении;</p>
<p>•	В начале соревнований, гонки, марафона - как дополнение к выстрелу стартового пистолета;
Из холодных фонтанов можно составить небольшую наземную композицию, например расположить их в форме сердца.</p>
<p>•	Креативные варианты использования холодных фонтанов: закрепление с небольшим уклоном к поверхности пола, парное размещение фонтанов рядом друг с другом, размещение фонтанов на потолке форсом вниз и т.д.</p>

