<h3>К самым популярным эффектам высотных фейерверков относятся:</h3><br>

<p> пион: сфера из ярких разноцветных звездочек. Самый распространенный эффект. Сфера может быть одноцветная или двухцветная, с мерцанием, треском и т.д.</p>
<br>
<p> георгин:  сфера из крупных звезд или жемчужин. Популярный эффект в крупнокалиберных фейерверках.</p>
<br>
<p> сердце: сфера из алых звезд в форме сердечка. Вариант эффекта - "двойное сердце".</p>
<br>
<p> хризантема:  сфера из разноцветных звездочек с искристыми хвостами, или "форсами", которые прочерчивают линии от центра к краям.</p>
<br>
<p> волна: сфера из мерцающих звездочек с коротким (короче, чем у хризантемы) форсом.</p>
<br>
<p> пальма или кокос:  разновидность хризантемы, состоящая из небольшого числа крупных звезд с очень яркими и мощными хвостами; каждая звезда представляет собой небольшую комету.</p>
<br>
<p> тысяча хризантем:  залп из большого числа мелких хризантем, как правило золотого цвета с оглушительным треском. В салютных батареях такой эффект часто является завершающим.</p>
<br>
<p> парча: разновидность хризантемы с более широким золотистым или серебряным форсом, который мерцает, подобно парчовой ткани.</p>
<br>
<p> камуро:  множество тонких форсов, как правило золотых, которые прочерчивают длинные линии от места вспышки практически до земли. Этот эффект особенно любим фотографами.</p>
<br>
<p> плакучая ива:  хризантемы с парчовым эффектом и продолжительной ниспадающей траекторией падения звездочек, результат похож на крону соответствующего дерева.</p>
<br>
<p> королевская ива:  крупные звезды с парчовым, достаточно коротким форсом, которые мягко разлетаются по ниспадающей траектории. Очень продолжительный по времени эффект.</p>
<br>
<p> корона:  разновидность "королевской ивы" с большим числом мелких звезд.</p>
<br>
<p> швермеры:  вертушки, отдельные элементы разлетаются в разные стороны по зигзагообразной траектории с характерным шипением.</p>
<br>
<p> пчелиный рой, рыбки:  множество мелких разноцветных точек, хаотично летающих в разные стороны, как рой потревоженных пчел.</p>
<br>
<p> змейки:  похожи на "рыбок", но каждый элемент более длинный, змейкам очень часто добавляют свистящий звуковой эффект.</p>
<br> 
<p> букеты, водопады: напоминают "мазок кисти" в определенном направлении; обычно в эффекте участвуют несколько таких "мазков", прочерченных в разнце стороны. Редкий и очень красивый эффект.</p>
<br>
<p> кроссеты: яркие звезды, которые вспыхивают на конце затухающих нитей. Иногда такая звезда имеет свою траекторию полета, что выглядит очень впечатляюще.
... и т.д.</p><br>



