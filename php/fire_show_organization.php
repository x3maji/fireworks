<h2>Высотные фейерверки</h2><br>
<p>Именно с яркими, разноцветными зонтиками высотных салютов у нас ассоциируется само понятие "праздничный фейерверк". Это потрясающее своей красотой и масштабом зрелище, которое знакомо всем нам с детства, ведь традиция устраивать высотные салюты на государственные праздники - 9 мая, 23 февраля и т.д. - идет еще с советских времен. Кто из нас, будучи ребенком, не ждал заветной минуты - 9 или 10 часов вечера - и не бежал сломя голову к окошку или на улицу, услышав характерное гулкое "БУММММММ!", а там... высоко в небе распускались дивные огненные цветы ярко-красного, изумрудного, белоснежного цвета; вспыхивали золотистые и серебряные искристые шары и мерцающие облака...</p><br>

<p>Высотные фейерверки можно назвать старшим братом всеми любимых салютных батарей. Они устроены практически одинаково: большое количество пусковых труб - мортир, в каждую из которых заложен пиротехнический заряд. Мортиры срабатывают одна за другой, в определенной последовательности, которую задает мастер-пиротехник. Каждый заряд вылетает из трубы на большую высоту, после чего взрывается ослепительной огненной сферой, производя один из множества красочных эффектов. Главным отличием высотных фейерверк-шоу является то, что "батарея" не является готовым изделием, а собирается вручную накануне пуска. Возможности безграничны: любая комбинация эффектов, любое количество залпов, любой калибр, вплоть до 12 дюймов (30 см)!</p><br><br>

